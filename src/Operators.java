public class Operators {

    int add(int a, int b) {
        int ans = a+b;
        return ans;          
    }

    int sub(int a, int b) {
        int ans = a-b;
        return ans;          
    }

    int mult(int a, int b) {
        int ans = a*b;
        return ans;          
    }

    double div(double a, double b) {
        double ans = a/b;
        return ans;          
    }
	
}